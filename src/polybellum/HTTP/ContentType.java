package polybellum.HTTP;

/**
 * An enum for common HTTP Content-type values
 * 
 * @author Nic Wilson (mtear)
 *
 */
public enum ContentType {
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// ENUM VALUES /////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * JSON strings
	 */
	JSON("application/json"),
	/**
	 * Plain text values
	 */
	TEXT_PLAIN("text/plain");
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// MEMBER VARIABLES //////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * The string value for the ContentType enum
	 */
	String _value = "";
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// CONSTRUCTORS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * Set the String value for this enum
	 * @param value
	 */
	ContentType(String value){
		_value = value;
	}

/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// PUBLIC METHODS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * Get the String value of the Content-type
	 * @return
	 */
	public String getValue(){
		return _value;
	}
	
}
