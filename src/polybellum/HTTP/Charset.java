package polybellum.HTTP;

/**
 * An enum to hold values representing different String encodings
 * 
 * @author Nic Wilson (mtear)
 *
 */
public enum Charset {
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// ENUM VALUES /////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * UTF-8 encoding
	 */
	UTF_8("UTF-8");

/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// MEMBER VARIABLES //////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * Hold an internal String representation of the encoding
	 */
	private String _value;

/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// CONSTRUCTORS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/
	
	/**
	 * Store the String value in the constructor
	 * 
	 * @param value The string value for the enumeration
	 */
	Charset(String value){
		_value = value;
	}
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// PUBLIC METHODS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/
	
	/**
	 * Get the encoding value as a String
	 */
	public String toString(){
		return _value;
	}
}
