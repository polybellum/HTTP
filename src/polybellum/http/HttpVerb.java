package polybellum.http;

/**
 * An enum for the different HTTP verbs this package supports
 * 
 * @author Nic Wilson (mtear)
 *
 */
public enum HttpVerb {
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// ENUM VALUES /////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * The GET method
	 */
	GET("GET"),
	/**
	 * The POST method
	 */
	POST("POST"),
	/**
	 * The HEAD method
	 */
	HEAD("HEAD"),
	/**
	 * The OPTIONS method
	 */
	OPTIONS("OPTIONS"),
	/**
	 * The PUT method
	 */
	PUT("PUT"),
	/**
	 * The DELETE method
	 */
	DELETE("DELETE"),
	/**
	 * The TRACE method
	 */
	TRACE("TRACE");
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// MEMBER VARIABLES //////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/
	
	/**
	 * The String value for this verb
	 */
	private String _verb;
	
/*__///////////////////////////////////////////////////////////////////////////////////////////////
____///////////////////////////////// CONSTRUCTORS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/

	/**
	 * Set the enum's String value for the Http verb
	 * @param verb
	 */
	HttpVerb(String verb){
		_verb = verb;
	}

/*__///////////////////////////////////////////////////////////////////////////////////////////////
____/////////////////////////////// PUBLIC METHODS ////////////////////////////////////////////////
____/////////////////////////////////////////////////////////////////////////////////////////////*/
	
	/**
	 * Get the verb's value as a String
	 */
	public String toString(){
		return _verb;
	}
}
